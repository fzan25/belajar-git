// Program sederhana: Kalkulator penjumlahan dua angka

// Meminta input dari user
let angka1 = parseInt(prompt("Masukkan angka pertama:"));
let angka2 = parseInt(prompt("Masukkan angka kedua:"));

// Menjumlahkan kedua angka
let hasil = angka1 + angka2;

// Menampilkan hasil
console.log(`Hasil penjumlahan ${angka1} + ${angka2} = ${hasil}`);
alert(`Hasil penjumlahan ${angka1} + ${angka2} = ${hasil}`);
